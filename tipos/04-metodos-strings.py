animal = "el pajaro esta feliz"
print(animal.upper())  # pone todo en mayusculas
print(animal.lower())  # pone todo en minuscula
print(animal.capitalize())  # pone la primera letra en Mayuscula
print(animal.title())  # pone la primera letra de cada palabra en mayuscula
print(animal.strip())  # remueve los espacio de derecha y izquierda
print(animal.capitalize().strip())  # uni varios
print(animal.find("esta"))  # devuelve en dodode se encuentra el indice
print(animal.replace("ro", "lo"))  # remplaza una letra palabra
print("esta" in animal)  # busca si esta o no esta esa palabra "bolean"
