import math

print(round(1.3))  # redondea al numero mas sercano
print(abs(-88))  # devuelve el numero positivo
print(math.ceil(1.1))
print(math.floor(1.2))  # numero entero mas sercano hacia abajo
print(math.isnan(23))  # comprueba si el numero no es falso
print(math.pow(10, 3))  # eleva un numero a  la potencia
print(math.sqrt(9))  # saca la raiz cuadrada
